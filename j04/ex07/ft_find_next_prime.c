int	ft_find_next_prime(int nb)
{
	int i;

	i = 2;
	if (nb < 2)
		return (2);
	while (i < nb)
	{
		if (nb == ((nb / i) * i))
		{
			nb++;
			i = 2;
		}
		i++;
	}
	return (nb);
}
