int		ft_count_if(char **tab, int (*f)(char*))
{
	int i;
	int j;
	int k;

	i = 0;
	k = 0;
	while (tab[i] != (void*)0)
	{
		j = f(tab[i]);
		if (j == 1)
			k++;
		i++;
	}
	return (k);
}
